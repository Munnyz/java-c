    private void Logar(){
            try {

            String nome, sobrenome;

            nome = txt_nome.getText();
            sobrenome = txt_sob.getText();

            UsuarioDTO userdto = new UsuarioDTO();
            userdto.setNome(nome);
            userdto.setSobrenome(sobrenome);

            UsuarioDAO userdao = new UsuarioDAO();
            ResultSet rsuserdao = userdao.autUser(userdto);

            if (rsuserdao.next()) {
                // chamar tela
                frmPrincipalView objfrmprincipalview = new frmPrincipalView();
                objfrmprincipalview.setVisible(true);
                dispose();
            } else{
                // enviar msg errado
                JOptionPane.showMessageDialog(null, "Usuario ou senha Inv√°lida");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "frmloginview" + ex);
        }
    }

